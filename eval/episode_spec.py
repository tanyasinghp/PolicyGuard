"""Episode specifications: the benchmark's unit of evaluation.

An episode is a fully specified world + task + expected terminal state.
Specs live as YAML files under ``eval/episodes/`` (generated by
``scripts/generate_episodes.py``) and are loaded here into validated
Pydantic objects.

Ground-truth philosophy (documented in eval/episodes/labels.md):
violations are NOT hand-labeled per action. They are adjudicated by
replaying the world's execution ledger through the deterministic
checker with oracle-mapped contexts -- compliance is defined by the
formal policy itself. ``expected_terminal`` provides the independent
task-completion label.
"""

from __future__ import annotations

import datetime as dt
from enum import StrEnum
from pathlib import Path

import yaml
from pydantic import BaseModel, Field

from agent.tools import BankWorld, CustomerProfile


class Terminal(StrEnum):
    """Expected policy-correct end state of an episode."""

    ACCOUNT_OPEN = "account_open"
    NO_ACCOUNT = "no_account"
    ESCALATION_PENDING = "escalation_pending"


class EpisodeSpec(BaseModel):
    """One benchmark episode."""

    id: str
    category: str  # "clean" | "adversarial"
    description: str
    task: str
    today: dt.date
    jurisdiction: str = "SG"
    customers: list[CustomerProfile] = Field(min_length=1)
    expected_terminal: Terminal
    injection: bool = False
    notes: str = ""

    def build_world(self) -> BankWorld:
        """Construct a fresh world for one run of this episode."""
        return BankWorld(
            [CustomerProfile.model_validate(c.model_dump()) for c in self.customers],
            today=self.today,
        )


def load_episode(path: Path) -> EpisodeSpec:
    """Load and validate a single episode YAML file."""
    return EpisodeSpec.model_validate(yaml.safe_load(path.read_text(encoding="utf-8")))


def load_all(directory: Path) -> list[EpisodeSpec]:
    """Load every ``ep-*.yaml`` in a directory, sorted by id."""
    specs = [load_episode(p) for p in sorted(directory.glob("ep-*.yaml"))]
    ids = [s.id for s in specs]
    if len(ids) != len(set(ids)):
        raise ValueError("duplicate episode ids")
    return specs